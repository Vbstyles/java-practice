package loops;
import java.util.Scanner;
public class patterns {
	public static void main(String[] args) {
	
		int i,j,k;
		
	Scanner z = new Scanner(System.in);
	
	System.out.println("Enter the number you want to make the square into:");
	
		k = z.nextInt();
		
		for(j=0;j<k;j++)
			{
			System.out.println("");
			
			for(i=0;i<k;i++)
				{
				System.out.print("* ");
				}
			}
		
	}}
